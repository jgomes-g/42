#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int		i;
	int		value;
	char	c;

	i = 0;
	value = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < 65 || (c > 90 && c < 97) || c > 122)
		{
			value = 0;
		}
		i++;
	}
	return (value);
}
