#include <unistd.h>

int	ft_atoi(char *s)
{
	int		sign;
	long	ret;

	ret = 0;
	sign = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	while (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign *= -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		ret = ret * 10 + *s - '0';
		s++;
	}
	return (sign * (int)ret);
}
